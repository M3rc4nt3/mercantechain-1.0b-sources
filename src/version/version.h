#define MERCANTE_BUILD_DESC MERCANTE_BUILD_DESC_FROM_UNKNOWN(MERCANTE_VERSION_MAJOR, MERCANTE_VERSION_MINOR, MERCANTE_VERSION_REVISION, MERCANTE_VERSION_BUILD)
#endif
#endif

